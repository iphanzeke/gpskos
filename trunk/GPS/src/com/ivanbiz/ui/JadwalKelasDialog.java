    private void refresh() {
        try {
            jDateChooserDariTanggal.setDate(new Date());
            jDateChooserSampaiTanggal.setDate(new Date());
            listPengajar = pengajarDAO.getAll(Pengajar.class);
            Pengajar pengajar = new Pengajar();
            pengajar.setNama("ALL");
            listPengajar.add(0, pengajar);
            updateComboPengajar();
        } catch (Exception ex) {
            Logger.getLogger(JadwalKelasDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void updateComboPengajar() {
        Object data[] = new Object[listPengajar.size()];
        int x = 0;
        for (Pengajar pengajar : listPengajar) {
            data[x] = pengajar.getNama();
            x++;
        }
        comboPengajar.setModel(new DefaultComboBoxModel(data));
    }
