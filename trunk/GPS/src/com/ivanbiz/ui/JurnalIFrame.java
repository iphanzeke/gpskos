    private void updateTableJurnal() {
        String[] judul = {"No", "Accounting Reference", "Date Reference", "Transaction Reference", "Debet", "Credit", "Currency", "GL Account", "Status"};
        Object[][] isi = new Object[listJurnal.size()][9];
        int x = 0;
        int no = 0;
        for (Jurnal jurnals : listJurnal) {
            no += 1;
            isi[x][0] = no;
            isi[x][1] = jurnals.getAccountingReference();
            isi[x][2] = dateFormat.format(jurnals.getDateReference());
            isi[x][3] = jurnals.getTransactionReference();
            isi[x][4] = jurnals.getDebit();
            isi[x][5] = jurnals.getCredit();
            isi[x][6] = jurnals.getCurrency();
            isi[x][7] = jurnals.getGLAccount();
            isi[x][8] = jurnals.getStatus();
            x++;
        }
        new ServiceHelper().setAutoRize(isi, judul, tableJurnal);
    }
