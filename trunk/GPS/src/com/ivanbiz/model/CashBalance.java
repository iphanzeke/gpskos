package com.ivanbiz.model;

import java.util.Date;

/**
 *
 * @author City_Z
 */
public class CashBalance {
    private long id;
    private GLAccount glAccount;
    private Date dateBalance;
    private double balance;
    private String status; /*0.Open 1.Close*/

    /**
     * @return the id
     */
    public long getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(long id) {
        this.id = id;
    }

    /**
     * @return the glAccount
     */
    public GLAccount getGlAccount() {
        return glAccount;
    }

    /**
     * @param glAccount the glAccount to set
     */
    public void setGlAccount(GLAccount glAccount) {
        this.glAccount = glAccount;
    }

    

    /**
     * @return the balance
     */
    public double getBalance() {
        return balance;
    }

    /**
     * @param balance the balance to set
     */
    public void setBalance(double balance) {
        this.balance = balance;
    }

    /**
     * @return the status
     */
    public String getStatus() {
        return status;
    }

    /**
     * @param status the status to set
     */
    public void setStatus(String status) {
        this.status = status;
    }

    /**
     * @return the dateBalance
     */
    public Date getDateBalance() {
        return dateBalance;
    }

    /**
     * @param dateBalance the dateBalance to set
     */
    public void setDateBalance(Date dateBalance) {
        this.dateBalance = dateBalance;
    }
    
    
}
