package com.ivanbiz.model;

/**
 *
 * @author City_Z
 */
public class GLAccount {
    private long id;
    private String nama;
    private String deskripsi;
    private String kode;
    private String noGL;
    private String nameGL;
    private String groupACC;
    /**
     * @return the id
     */
    public long getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(long id) {
        this.id = id;
    }

    /**
     * @return the noGL
     */
    public String getNoGL() {
        return noGL;
    }

    /**
     * @param noGL the noGL to set
     */
    public void setNoGL(String noGL) {
        this.noGL = noGL;
    }

    /**
     * @return the nameGL
     */
    public String getNameGL() {
        return nameGL;
    }

    /**
     * @param nameGL the nameGL to set
     */
    public void setNameGL(String nameGL) {
        this.nameGL = nameGL;
    }

    /**
     * @return the groupACC
     */
    public String getGroupACC() {
        return groupACC;
    }

    /**
     * @param groupACC the groupACC to set
     */
    public void setGroupACC(String groupACC) {
        this.groupACC = groupACC;
    }

    /**
     * @return the nama
     */
    public String getNama() {
        return nama;
    }

    /**
     * @param nama the nama to set
     */
    public void setNama(String nama) {
        this.nama = nama;
    }

    /**
     * @return the deskripsi
     */
    public String getDeskripsi() {
        return deskripsi;
    }

    /**
     * @param deskripsi the deskripsi to set
     */
    public void setDeskripsi(String deskripsi) {
        this.deskripsi = deskripsi;
    }

    /**
     * @return the kode
     */
    public String getKode() {
        return kode;
    }

    /**
     * @param kode the kode to set
     */
    public void setKode(String kode) {
        this.kode = kode;
    }
    
}
