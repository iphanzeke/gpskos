    public BiayaLainReportDialog(Date dari, Date sampai) {
        try {
            initComponents();
            dateFormat1 = new SimpleDateFormat("dd-MMMM-yyyy");
            jLabelJudul.setText("Dari tanggal " + dateFormat1.format(dari) + " sampai tanggal " + dateFormat1.format(sampai));
            numberFormat = NumberFormat.getNumberInstance();
            dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            gLAccountDAO = new GLAccountDAOImpl();
            jurnalDAO = new JurnalDAOImpl();
            listGLAccounts = gLAccountDAO.getDataByEquals(GLAccount.class, "groupACC", "BiayaLain");
            listJurnal = new ArrayList();
            for (GLAccount gLAccount : listGLAccounts) {
                Jurnal jurnal = new Jurnal();
                jurnal.setTransactionReference(gLAccount.getNameGL());
                jurnal.setGLAccount(gLAccount.getNoGL());
                jurnal.setCredit(jurnalDAO.getSumCreditGLAccount(dateFormat.format(dari), dateFormat.format(sampai), gLAccount.getNoGL()));
                listJurnal.add(jurnal);
            }
            updateTableJurnal(listJurnal);
        } catch (Exception ex) {
            Logger.getLogger(BiayaLainReportDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

