    private void updateTableJurnal(List<Jurnal> listJurnal) {
        String[] judul = {"No", "No GL", "Nama GL", "Jumlah"};
        Object[][] isi = new Object[listJurnal.size()][4];
        int x = 0;
        int no = 0;
        for (Jurnal jurnal : listJurnal) {
            no += 1;
            isi[x][0] = no;
            isi[x][1] = jurnal.getGLAccount();
            isi[x][2] = jurnal.getTransactionReference();
            isi[x][3] = numberFormat.format(jurnal.getCredit());
            x++;
        }
        new ServiceHelper().setAutoRize(isi, judul, tableJurnal);
    }
