    private void buttonUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonUbahActionPerformed
        if (tableBank.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Pilih salah satu data", "warning", JOptionPane.WARNING_MESSAGE);
        } else {
            bank = listBank.get(tableBank.getSelectedRow());
            textNama.setText(bank.getNama());
            textDeskripsi.setText(bank.getDeskripsi());
            labelBank.setText("Ubah Bank");
            renderBankUpdateDialog();            
        }
    }//GEN-LAST:event_buttonUbahActionPerformed

    private void buttonHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonHapusActionPerformed
        if (tableBank.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Pilih salah satu data", "warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                bank = listBank.get(tableBank.getSelectedRow());
                bankDAO.delete(bank);
                refresh();
            } catch (Exception ex) {
                Logger.getLogger(BankIFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_buttonHapusActionPerformed

