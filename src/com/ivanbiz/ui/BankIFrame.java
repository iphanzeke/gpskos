    private void refresh() {
        try {
            listBank = bankDAO.getAll(Bank.class);
            updateTableBank();
        } catch (Exception ex) {
            Logger.getLogger(BankIFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void renderBankUpdateDialog() {
        BankUpdateDialog.setSize(400, 300);
        BankUpdateDialog.setVisible(true);
        BankUpdateDialog.setLocationRelativeTo(this);
        BankUpdateDialog.setModal(true);
    }

    private void renderHakAkses(List<AksesMatrix> listAksesMatrix) {
        buttonTambah.setEnabled(MenuAksesConstant.validate(MenuAksesConstant.TAMBAH_BANK, listAksesMatrix));
        buttonUbah.setEnabled(MenuAksesConstant.validate(MenuAksesConstant.UBAH_BANK, listAksesMatrix));
        buttonHapus.setEnabled(MenuAksesConstant.validate(MenuAksesConstant.HAPUS_BANK, listAksesMatrix));
    }

    private void updateTableBank() {
        String[] judul = {"No", "Nama", "Deskripsi"};
        Object[][] isi = new Object[listBank.size()][9];
        int x = 0;
        int no = 0;
        for (Bank banks : listBank) {
            no += 1;
            isi[x][0] = no;
            isi[x][1] = banks.getNama();
            isi[x][2] = banks.getDeskripsi();
            x++;
        }
        new ServiceHelper().setAutoRize(isi, judul, tableBank);
    }

    private void validate(Bank bank) {
        if (bank == null) {
            JOptionPane.showMessageDialog(this, "Bank tidak boleh null");
        } else if (bank.getNama() == null) {
            JOptionPane.showMessageDialog(this, "Nama tidak boleh null");
        } else if (bank.getNama().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama tidak boleh kosong");
        } else if (bank.getDeskripsi() == null) {
            JOptionPane.showMessageDialog(this, "Deskripsi tidak boleh null");
        } else if (bank.getDeskripsi().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Deskripsi tidak boleh kosong");
        } else {
            try {
                bankDAO.saveOrUpdate(bank);
                BankUpdateDialog.dispose();
                refresh();
            } catch (Exception ex) {
                Logger.getLogger(BankIFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
