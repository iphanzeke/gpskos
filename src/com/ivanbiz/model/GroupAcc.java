package com.ivanbiz.model;

/**
 *
 * @author City_Z
 */
public class GroupAcc {
    private long id;
    private String namaGroup;
    private String deskripsi;
    
    /**
     * @return the id
     */
    public long getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(long id) {
        this.id = id;
    }

    /**
     * @return the namaGroup
     */
    public String getNamaGroup() {
        return namaGroup;
    }

    /**
     * @param namaGroup the namaGroup to set
     */
    public void setNamaGroup(String namaGroup) {
        this.namaGroup = namaGroup;
    }

    /**
     * @return the deskripsi
     */
    public String getDeskripsi() {
        return deskripsi;
    }

    /**
     * @param deskripsi the deskripsi to set
     */
    public void setDeskripsi(String deskripsi) {
        this.deskripsi = deskripsi;
    }

      
    
}
